// import java classes
import java.util.*;

public class ForLoopPractice {

    public static void main(String[] args) 
    {
        
        //make your name output 20 times  
        
        for (int i=1 ; i<=20 ; i++)
        {
            
            System.out.println("\nSalman Shaikh "+ i);
        }
        
        //output all even number from one to hundred
        
        for (int n =0; n<=100; n+=2)
        
        {
            System.out.println("\n"+n);
            
        }
        
    }
}
